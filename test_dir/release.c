/*==================================================================*/
/* Release() ("C" Interface)                                        */
/*==================================================================*/
void disabl_(int      * vector);

void Release(void)
{
  int              zero;                 /* A Zero Value            */

  zero           = 0L;                   /* Insure is zero          */
  (void) disabl_(&zero);                 /* Invoke the command      */
  return;                                /* Return to caller        */
}                                        /*                         */

/*==================================================================*/
/* Release() (FORTRAN Interface)                                    */
/*==================================================================*/

void release_(void)
{
  Release();                             /* Do the release          */
  return;                                /* Return to the caller    */
}                                        /*                         */

